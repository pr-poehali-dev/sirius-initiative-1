let fallbackIndex = 0

export function askNothingAI(question: string): Promise<string> {
  const q = question.toLowerCase()

  const matched = rules.find(r => r.keywords.some(kw => q.includes(kw)))

  const raw = matched
    ? (typeof matched.answer === 'function' ? matched.answer(q) : matched.answer)
    : fallback[fallbackIndex++ % fallback.length]

  return new Promise(resolve => {
    setTimeout(() => resolve(raw), 600 + Math.random() * 600)
  })
}
